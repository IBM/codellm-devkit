static void
rtype_to_howto (abfd, cache_ptr, dst)
     bfd *abfd;
     arelent *cache_ptr;
     Elf_Internal_Rela *dst;
{
  BFD_ASSERT (ELF32_R_TYPE(dst->r_info) < (unsigned int) R_68K__max);
  cache_ptr->howto = &howto_table[ELF32_R_TYPE(dst->r_info)];
}

